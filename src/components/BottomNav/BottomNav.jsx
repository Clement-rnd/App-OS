const TABS = [
  { key: 'home', label: 'Accueil', Icon: HouseIcon },
  { key: 'chat', label: 'Mes Avis', Icon: ChatIcon },
  { key: 'send', label: 'Récolter', Icon: SendIcon },
  { key: 'user', label: 'Mon Profil', Icon: UserIcon },
]

export function BottomNav({ active, onNavigate, badges = {} }) {
  return (
    <nav className="bottom-nav">
      <div className="bottom-nav__row">
        {TABS.map(tab => {
          const isActive = tab.key === active
          return (
            <button
              key={tab.key}
              type="button"
              className={`bottom-nav__item${isActive ? ' bottom-nav__item--active' : ''}`}
              onClick={() => onNavigate?.(tab.key)}
            >
              <span className="bottom-nav__icon-wrap">
                <tab.Icon />
                {badges[tab.key] > 0 && <span className="bottom-nav__badge">{badges[tab.key]}</span>}
              </span>
              <span className="bottom-nav__label">{tab.label}</span>
            </button>
          )
        })}
      </div>
      <div className="bottom-nav__home-indicator-wrap" />
    </nav>
  )
}
